SELECT DISTINCT MC.SEGMENT2 AS DISPLAY_VALUE, MC.SEGMENT2 AS RETURN_VALUE
  FROM INV.MTL_CATEGORIES_B MC, APPS.MTL_CATEGORY_SETS MCS
 WHERE     1 = 1
       AND MC.STRUCTURE_ID = MCS.STRUCTURE_ID
       AND MCS.CATEGORY_SET_ID = 1;


SELECT DISTINCT MC.SEGMENT3 AS DISPLAY_VALUE, MC.SEGMENT3 AS RETURN_VALUE
  FROM INV.MTL_CATEGORIES_B MC, APPS.MTL_CATEGORY_SETS MCS
 WHERE     1 = 1
       AND MC.STRUCTURE_ID = MCS.STRUCTURE_ID
       AND MCS.CATEGORY_SET_ID = 1;



